package com.fourth_class.loop.classwork;

public class InnerForLoop {
    public static void main(String[] args) {

//        printing Hello, this is for inner loop

        for(int i=0;i<3;i++){
            for(int j=0;j<3;j++){
                System.out.println("Hello, this is for inner loop");
            }
        }
    }
}
