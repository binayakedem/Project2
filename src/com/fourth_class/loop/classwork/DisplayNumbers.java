package com.fourth_class.loop.classwork;

public class DisplayNumbers {
    public static void main(String[] args) {
//        displaying numbers from 0 to 10

//        number using for loop
        System.out.println("Printing number using for loop");
        for(int i=0;i<10;i++){
            System.out.println((i+1));
        }
        int i=0;
        System.out.println("Number using the while loop");
        while(i<10){
            System.out.println((i+1));
            i++;

        }
    }
}
